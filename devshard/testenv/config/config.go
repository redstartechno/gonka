// Load reads a YAML config file and applies defaults.
func Load(path string) (*File, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("read config %s: %w", path, err)
	}
	var cfg File
	if err := yaml.Unmarshal(data, &cfg); err != nil {
		return nil, fmt.Errorf("parse config: %w", err)
	}
	cfg.ApplyDefaults()
	return &cfg, nil
}

// Save marshals the config back to YAML at path.
func (c *File) Save(path string) error {
	data, err := yaml.Marshal(c)
	if err != nil {
		return fmt.Errorf("marshal config: %w", err)
	}
	return os.WriteFile(path, append([]byte(generatedBanner), data...), 0o644)
}

// ApplyDefaults fills unset operational and seed defaults.
func (c *File) ApplyDefaults() {
	if c.ChainID == "" {
		c.ChainID = DefaultChainID
	}
	if c.BlockHeight == 0 {
		c.BlockHeight = DefaultBlockHeight
	}
	if c.Epoch.Index == 0 {
		c.Epoch.Index = 1
	}
	if c.Epoch.PocStartBlockHeight == 0 {
		c.Epoch.PocStartBlockHeight = c.BlockHeight - 50
	}
	if c.Params.LogprobsMode == "" {
		c.Params.LogprobsMode = "raw"
	}
	if c.Params.MaxNonce == 0 {
		c.Params.MaxNonce = 500
	}
	if c.Params.ValidationRate == 0 {
		c.Params.ValidationRate = 6000
	}
	if c.Params.VoteThresholdFactor == 0 {
		c.Params.VoteThresholdFactor = 50
	}
	if !c.Params.DevshardRequestsEnabled {
		c.Params.DevshardRequestsEnabled = true
	}

	if c.MockChain.GRPCPort == 0 {
		c.MockChain.GRPCPort = DefaultMockChainGRPCPort
	}
	if c.MockChain.RPCPort == 0 {
		c.MockChain.RPCPort = DefaultMockChainRPCPort
	}
	if c.MockChain.Host == "" {
		c.MockChain.Host = DefaultMockChainHost
	}
	if c.MockChain.TestenvPort == 0 {
		c.MockChain.TestenvPort = DefaultMockChainTestenvPort
	}

	if c.MockDapi.GRPCPort == 0 {
		c.MockDapi.GRPCPort = DefaultMockDapiGRPCPort
	}
	if c.MockDapi.HTTPPort == 0 {
		c.MockDapi.HTTPPort = DefaultMockDapiHTTPPort
	}
	if c.MockDapi.Host == "" {
		c.MockDapi.Host = DefaultMockDapiHost
	}

	if c.MockOpenAI.HTTPPort == 0 {
		c.MockOpenAI.HTTPPort = DefaultMockOpenAIHTTPPort
	}
	if c.MockOpenAI.Host == "" {
		c.MockOpenAI.Host = DefaultMockOpenAIHost
	}

	if c.Versiond.VersionName == "" {
		c.Versiond.VersionName = DefaultVersionName
	}
	if c.Versiond.HostBinaryMount == "" {
		c.Versiond.HostBinaryMount = DefaultDevsharddHostMount
	}
	if c.Versiond.OverridePath == "" {
		c.Versiond.OverridePath = DefaultDevsharddOverride
	}
	if c.Versiond.PollInterval == "" {
		c.Versiond.PollInterval = DefaultVersiondPoll
	}
	if c.Versiond.BinaryVersion == "" {
		c.Versiond.BinaryVersion = DefaultBinaryVersion
	}
	if c.Versiond.KeyringDir == "" {
		c.Versiond.KeyringDir = DefaultKeyringDir
	}
	if c.Versiond.KeyringPassword == "" {
		c.Versiond.KeyringPassword = DefaultKeyringPassword
	}
	if c.Versiond.Mode == "" {
		if len(c.Hosts) <= 1 {
			c.Versiond.Mode = VersiondModeSingle
		} else {
			c.Versiond.Mode = VersiondModeMulti
		}
	}

	if c.VersiondRouter.Host == "" {
		c.VersiondRouter.Host = DefaultVersiondRouterHost
	}
	if c.VersiondRouter.Port == 0 {
		c.VersiondRouter.Port = DefaultVersiondRouterPort
	}

	if c.Devshardctl.Host == "" {
		c.Devshardctl.Host = DefaultDevshardctlHost
	}
	if c.Devshardctl.Port == 0 {
		c.Devshardctl.Port = DefaultUserPort
	}

	if c.Postgres.Host == "" {
		c.Postgres.Host = DefaultPostgresHost
	}
	if c.Postgres.Port == 0 {
		c.Postgres.Port = DefaultPostgresPort
	}
	if c.Postgres.Database == "" {
		c.Postgres.Database = DefaultPostgresDB
	}
	if c.Postgres.User == "" {
		c.Postgres.User = DefaultPostgresUser
	}
	if c.Postgres.Password == "" {
		c.Postgres.Password = DefaultPostgresPassword
	}

	if c.Escrow.Slots == 0 {
		if n := len(c.Hosts); n > 0 {
			c.Escrow.Slots = n
		} else {
			c.Escrow.Slots = DefaultEscrowSlots
		}
	}
	if c.Escrow.SlotURL == "" {
		c.Escrow.SlotURL = RouterBaseURL(c)
	}

	if c.User.Port == 0 {
		c.User.Port = DefaultUserPort
	}
	if c.Network.Subnet == "" {
		c.Network.Subnet = DefaultNetworkCIDR
	}
	if c.Network.BaseIP == "" {
		c.Network.BaseIP = DefaultNetworkBaseIP
	}

	for i := range c.Hosts {
		if c.Hosts[i].Port == 0 {
			c.Hosts[i].Port = DefaultHostPort
		}
	}
}

// RouterBaseURL returns the in-network HTTP origin for versiond-router (container port).
// Host-published ports (e.g. citest 18080) are for localhost probes only; see EndpointsFromConfig.
func RouterBaseURL(c *File) string {
	if c == nil {
		return DefaultEscrowSlotURL
	}
	host := c.VersiondRouter.Host
	if host == "" {
		host = DefaultVersiondRouterHost
	}
	return fmt.Sprintf("http://%s:%d", host, DefaultHostPort)
}

// PrimaryEscrowID returns the seeded escrow id for single-escrow gateway bootstrap.
func PrimaryEscrowID(c *File) string {
	if c == nil || len(c.Escrows) == 0 || c.Escrows[0].ID == 0 {
		return "1"
	}
	return fmt.Sprintf("%d", c.Escrows[0].ID)
}

// PrimaryModelID returns the model id used by the seeded escrow and gateway default.
func PrimaryModelID(c *File) string {
	if c == nil {
		return DefaultModelID
	}
	if len(c.Escrows) > 0 && c.Escrows[0].ModelID != "" {
		return c.Escrows[0].ModelID
	}
	return DefaultModelID
}

// Validate enforces invariants gencompose and mock-chain rely on.
func (c *File) Validate() error {
	if c.ChainID == "" {
		return errors.New("chain_id must not be empty")
	}
	if len(c.Hosts) == 0 {
		return errors.New("at least one host is required")
	}
	if c.Escrow.Slots < len(c.Hosts) {
		return fmt.Errorf("escrow.slots (%d) must be >= number of hosts (%d)",
			c.Escrow.Slots, len(c.Hosts))
	}
	for i, h := range c.Hosts {
		if strings.TrimSpace(h.Address) == "" {
			return fmt.Errorf("hosts[%d].address must be set (run gencompose)", i)
		}
		if strings.TrimSpace(h.PrivateKeyHex) == "" {
			return fmt.Errorf("hosts[%d].private_key_hex must be set (run gencompose)", i)
		}
	}
	if strings.TrimSpace(c.User.Address) == "" || strings.TrimSpace(c.User.PrivateKeyHex) == "" {
		return errors.New("user key and address must be set (run gencompose)")
	}
	if c.Versiond.Mode == VersiondModeMulti && !c.Postgres.Enabled {
		return errors.New("versiond.mode multi requires postgres.enabled: true (shared payload/session store for multiple versiond hosts)")
	}
	if c.Versiond.Mode == VersiondModeSingle && c.Postgres.Enabled {
		return errors.New("versiond.mode single must use postgres.enabled: false (file payload fallback); use mode multi for shared Postgres")
	}
	return nil
}

// ChainSeed returns the subset mock-chain seed.Load consumes.
func (c *File) ChainSeed() *File {
	out := *c
	return &out
}
