// VersiondRouterCfg is the sticky nginx router in front of versiond instances.
type VersiondRouterCfg struct {
	Host string `yaml:"host"`
	Port int    `yaml:"port"`
	IP   string `yaml:"ip"`
}

// DevshardctlCfg is the gateway service (Phase 7 wires REST paths).
type DevshardctlCfg struct {
	Host string `yaml:"host"`
	Port int    `yaml:"port"`
	IP   string `yaml:"ip"`
}

// PostgresCfg is optional shared storage for devshardd children.
type PostgresCfg struct {
	Enabled  bool   `yaml:"enabled"`
	Host     string `yaml:"host"`
	Port     int    `yaml:"port"`
	Database string `yaml:"database"`
	User     string `yaml:"user"`
	Password string `yaml:"password"`
	IP       string `yaml:"ip"`
}

// EscrowMeta describes slot layout gencompose owns (distinct from escrows[] seed).
type EscrowMeta struct {
	Slots   int    `yaml:"slots"`
	SlotURL string `yaml:"slot_url"`
}

// HostCfg is one future versiond/devshardd participant.
type HostCfg struct {
	ID            string `yaml:"id"`
	PrivateKeyHex string `yaml:"private_key_hex"`
	Address       string `yaml:"address"`
	SlotIDs       []int  `yaml:"slot_ids"`
	URL           string `yaml:"url"`
	Port          int    `yaml:"port"`
	IP            string `yaml:"ip"`
}

// UserCfg is the devshardctl operator identity.
type UserCfg struct {
	PrivateKeyHex string `yaml:"private_key_hex"`
	Address       string `yaml:"address"`
	Port          int    `yaml:"port"`
}

// NetworkCfg holds docker-compose network knobs.
type NetworkCfg struct {
	Subnet string `yaml:"subnet"`
	BaseIP string `yaml:"base_ip"`
}

// WarmGranteeCfg is an optional warm-wallet grantee synced into grantees[].
type WarmGranteeCfg struct {
	PrivateKeyHex string `yaml:"private_key_hex"`
	Address       string `yaml:"address"`
}

